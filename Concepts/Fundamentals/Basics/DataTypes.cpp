#include <iostream>
using namespace std;

int main(){

    int a = 3;  // integer - 4 bytes
    cout<<a <<endl;

    char c = 'a';  // character - 1 byte - single quotes
    cout<<c <<endl;

    bool t = true;  // boolean - 1 byte
    bool f = false;
    cout<<t <<' ' <<f <<endl;

    float x = 3.3;  // floating point - 4 bytes 
    cout<<x <<endl;

    double d = 0.25;  // double - 8 bytes
    cout<<d <<endl; 

    return 0;
}